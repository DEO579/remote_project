package com.simple_program;

public class Literal {
public static void main(String[] args) {
	int integerCount=827;
	System.out.println(integerCount);//827
	int octaCount=067;
	System.out.println(octaCount);//55
	int hexaCount=0x67a;
	System.out.println(octaCount);//55
	int binaryCount=0b1101;
	System.out.println(binaryCount);//13
	char charCount='a';
	System.out.println(charCount);//13
	char character='\u0021';
	System.out.println(character);//13
}
}
